    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed

        if (emailTextField.getText().isEmpty() || String.valueOf(passTextField.getPassword()).isEmpty()) {
            new util.exceptions.ExceptionEmptyFields().setVisible(true);
        } else {
            String email = emailTextField.getText();
            String password = String.valueOf(passTextField.getPassword());

            //checks if the current email and pass is in the database for the given user session
            if (!new dataBaseSQL().checkAvailablity(email, password)) {
                // if credentials doesn't match then open a window
                new util.exceptions.ExceptionWrongCredential().setVisible(true);

            } else {

                if (JatraBegins.getUser().equals("passenger")) {
                    //open passenger homepage
                    new passenger.PassengerDashBoard().setVisible(true);
                    this.setVisible(false);

                } else {
                    //open owner homepage
                    new owner.OwnerDashboard().setVisible(true);
                    this.setVisible(false);
                }
            }
        }
    }//GEN-LAST:event_loginActionPerformed

