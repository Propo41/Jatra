package googlemapsapi.Places;

/**
 *
 * @author Swapnil
 */
public class LocationParser {

    final private double lat;
    final private double lng;

    public LocationParser(double lat, double lng) {
        this.lat = lat;
        this.lng = lng;
    }

    public double getLat() {
        return lat;
    }

    public double getLng() {
        return lng;
    }
}
